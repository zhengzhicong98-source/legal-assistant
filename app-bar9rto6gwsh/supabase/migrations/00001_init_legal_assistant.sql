
-- 维权机构表
CREATE TABLE rights_centers (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  province text NOT NULL,
  city text NOT NULL,
  name text NOT NULL,
  type text NOT NULL CHECK (type IN ('劳动仲裁委', '消费者协会', '法律援助中心')),
  address text,
  phone text,
  website text,
  process text,
  working_hours text,
  created_at timestamptz DEFAULT now()
);

-- 合同审查记录表
CREATE TABLE contract_reviews (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  file_url text,
  file_name text,
  review_result jsonb,
  created_at timestamptz DEFAULT now()
);

-- 禁用RLS（无登录系统）
ALTER TABLE rights_centers DISABLE ROW LEVEL SECURITY;
ALTER TABLE contract_reviews DISABLE ROW LEVEL SECURITY;

